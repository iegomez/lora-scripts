func makeForm() ui.Control {
	vbox := ui.NewVerticalBox()
	vbox.SetPadded(true)

	hbox := ui.NewHorizontalBox()
	hbox.SetPadded(true)
	vbox.Append(hbox, false)

	entryForm := ui.NewForm()
	entryForm.SetPadded(true)

	mqttURI := ui.NewEntry()
	mqttUser := ui.NewEntry()
	mqttPassword := ui.NewPasswordEntry()

	gw := ui.NewEntry()
	devEUI := ui.NewEntry()
	devAddr := ui.NewEntry()
	nwsKey := ui.NewEntry()
	asKey := ui.NewEntry()

	appEUI := ui.NewEntry()
	appKey := ui.NewEntry()

	btn := ui.NewButton("Run")

	btn.OnClicked(func(*ui.Button) {
		log.Printf("\nGW Mac: %s\nDevice address: %s\nNetwork session key: %s\nApplication session key: %s\n", gw.Text(), devAddr.Text(), nwsKey.Text(), asKey.Text())
	})

	hbox.Append(btn, false)

	vbox.Append(ui.NewHorizontalSeparator(), false)

	group := ui.NewGroup("Data")
	group.SetMargined(true)
	vbox.Append(group, true)

	group.SetChild(ui.NewNonWrappingMultilineEntry())
	group.SetChild(entryForm)

	entryForm.Append("MQTT broker uri:", mqttURI, false)
	entryForm.Append("MQTT user:", mqttUser, false)
	entryForm.Append("MQTT password:", mqttPassword, false)

	entryForm.Append("Gateway MAC", gw, false)
	entryForm.Append("Device EUI", devEUI, false)
	entryForm.Append("Device addres", devAddr, false)
	entryForm.Append("Network session key", nwsKey, false)
	entryForm.Append("Application session key", asKey, false)

	entryForm.Append("AppEUI", appEUI, false)
	entryForm.Append("AppKey", appKey, false)

	return vbox
}

